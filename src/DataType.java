public class DataType {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(i + " có kiểu dữ liệu là int.");
        float f = 20.5f;
        System.out.println(f + "f có kiểu dữ lệu là float.");
        double d = 20.5;
        System.out.println(d + " có kiểu dữ liệu là double.");
        boolean b = true;
        System.out.println(b + " có kiểu dữ liệu là boolean.");
        char c = 'a';
        System.out.println(c + " có kiểu dữ liệu là char");
        String s = "Hà Nội";
        System.out.println(s + " có kiểu dữ liệu là string.");
    }
}
